/**
 * Escrever um programa que leia três números nas variáveis x, y e z
 * e depois ordena x, y e z de forma que
 * x guarde o valor menor
 * y guarde o valor intermediário
 * z guarde o valor maior 
*/

#include <stdio.h>

int main() {
    int x, y, z;
    scanf("%d%d%d", &x, &y, &y);

    printf("%d\n%d\n%d\n", x, y, z);

    return 0;
}